package list;


class Xs {
	private String xingming;
	private int xuehao;
	private int chengji;
	
	Xs(String xingming, int xuehao, int chengji) {
		this.xingming = xingming;
		this.xuehao = xuehao;
		this.chengji = chengji;
	}

	public String getXingming() {
		return xingming;
	}

	public int getXuehao() {
		return xuehao;
	}

	public int getChengji() {
		return chengji;
	}
	
	
	
}
